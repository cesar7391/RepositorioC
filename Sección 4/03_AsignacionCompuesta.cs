using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Seccion4
{
    class Program
    {
        static void Main(string[] args)
        {
            float x = 2;

            Console.WriteLine(x);

            x += 5; // x = x + 5;

            Console.WriteLine(x);

            x -= 2; // x = x - 2;

            Console.WriteLine(x);

            x /= 2; // x = x / 2;

            Console.WriteLine(x);

            x *= 2; // x = x * 2;

            Console.WriteLine(x);


        }
    }
}
